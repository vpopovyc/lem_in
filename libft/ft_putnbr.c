#include "includes/libft.h"

void	ft_putnbr(int n)
{
	long	b;

	b = n;
	if (b < 0)
	{
		ft_putchar('-');
		b = -b;
	}
	if (b >= 10)
	{
		ft_putnbr((int)b / 10);
		ft_putnbr(b % 10);
	}
	if (b < 10)
		ft_putchar(b += 48);
}
