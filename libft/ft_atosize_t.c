#include "includes/libft.h"

size_t		ft_atosize_t(char *s)
{
	char	i;
	size_t	beer;

	beer = 0;
	i = 0;
	while (((*s >= '\t' && *s <= '\r') || *s == ' ') && *s)
		++s;
	(*s == '-') ? i = 1 : 0;
	(*s == '-' || *s == '+') ? ++s : 0;
	while ((*s >= '0' && *s <= '9') && *s)
	{
		beer = (beer * 10) + (*s - 48);
		++s;
	}
	if (i == 1)
		return (beer = -beer);
	else
		return (beer);
}
